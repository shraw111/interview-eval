"use client";

import { Card, CardContent } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import { EvaluationResult } from "@/types/evaluation";
import { EvaluationJourney } from "./EvaluationJourney";
import { CheckCircle, XCircle, AlertTriangle } from "lucide-react";

interface EvaluationResultsProps {
  result: EvaluationResult;
}

export function EvaluationResults({ result }: EvaluationResultsProps) {
  const { candidate_info, decision, metadata } = result;

  // Extract recommendation from decision text
  const getRecommendation = (): {
    text: string;
    variant: "default" | "secondary" | "destructive" | "outline";
    icon: React.ReactNode;
    gradient: string;
  } => {
    const lowerDecision = decision.toLowerCase();
    if (lowerDecision.includes("strong recommend") || lowerDecision.includes("strongly recommend")) {
      return {
        text: "STRONG RECOMMEND",
        variant: "default",
        icon: <CheckCircle className="w-6 h-6" />,
        gradient: "from-success to-success/80",
      };
    } else if (lowerDecision.includes("recommend") && !lowerDecision.includes("do not")) {
      return {
        text: "RECOMMEND",
        variant: "default",
        icon: <CheckCircle className="w-6 h-6" />,
        gradient: "from-primary to-primary/80",
      };
    } else if (lowerDecision.includes("borderline") || lowerDecision.includes("on the fence")) {
      return {
        text: "BORDERLINE",
        variant: "secondary",
        icon: <AlertTriangle className="w-6 h-6" />,
        gradient: "from-warning to-warning/80",
      };
    } else if (lowerDecision.includes("do not recommend") || lowerDecision.includes("not ready")) {
      return {
        text: "DO NOT RECOMMEND",
        variant: "destructive",
        icon: <XCircle className="w-6 h-6" />,
        gradient: "from-destructive to-destructive/80",
      };
    }
    return {
      text: "REVIEW REQUIRED",
      variant: "outline",
      icon: <AlertTriangle className="w-6 h-6" />,
      gradient: "from-muted to-muted/80",
    };
  };

  const recommendation = getRecommendation();

  return (
    <div className="space-y-8 max-w-5xl mx-auto">
      {/* Recommendation Card - Hero */}
      <Card className="shadow-card fade-in-delay-1">
        <CardContent className="pt-8 pb-8">
          <div className="flex flex-col md:flex-row items-center md:items-start justify-between gap-6">
            {/* Candidate Info */}
            <div className="flex-1 text-center md:text-left">
              <h3 className="text-3xl font-bold mb-2">{candidate_info.name}</h3>
              <div className="flex items-center gap-2 justify-center md:justify-start mb-4">
                <Badge variant="outline" className="text-xs">
                  {candidate_info.current_level}
                </Badge>
                <span className="text-muted-foreground">â†’</span>
                <Badge variant="outline" className="text-xs">
                  {candidate_info.target_level}
                </Badge>
              </div>
              <p className="text-sm text-muted-foreground">
                {candidate_info.years_experience} years at current level
              </p>
            </div>

            {/* Recommendation Badge - Large */}
            <div className="flex flex-col items-center gap-3">
              <Badge
                variant={recommendation.variant}
                className="flex items-center gap-2 px-6 py-3 text-base"
              >
                {recommendation.icon}
                <span className="font-semibold">
                  {recommendation.text}
                </span>
              </Badge>
            </div>
          </div>
        </CardContent>
      </Card>

      {/* AI Disclaimer */}
      <Card className="bg-amber-50 dark:bg-amber-950/20 border-amber-200 dark:border-amber-900/50 fade-in-delay-1">
        <CardContent className="pt-6 pb-6">
          <div className="flex items-start gap-4">
            <AlertTriangle className="w-5 h-5 text-amber-600 dark:text-amber-500 shrink-0 mt-0.5" />
            <div className="flex-1">
              <h4 className="text-sm font-semibold text-amber-900 dark:text-amber-100 mb-2">
                AI-Generated Evaluation
              </h4>
              <p className="text-sm text-amber-800 dark:text-amber-200 leading-relaxed">
                This evaluation was generated by AI and should be used as one input in your decision-making process.
                <strong className="font-semibold"> Human judgment is essential.</strong> If you observe discrepancies between the AI assessment and your own evaluation,
                trust your professional judgment and experience. Use this as a structured second opinion, not as a replacement for human review.
              </p>
            </div>
          </div>
        </CardContent>
      </Card>


      {/* Evaluation Journey */}
      <div className="fade-in-delay-3">
        <EvaluationJourney result={result} />
      </div>
    </div>
  );
}
